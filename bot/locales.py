# Multilingual support for Velox Bot

def _t(lang: str, key: str, **kwargs) -> str:
    l = (lang or "ru").lower()
    # Default to RU if not found or empty
    if l not in ["en", "ru"]: l = "ru"
    
    table = RU if l == "ru" else EN
    text = table.get(key, key)
    
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
            
    return text

